public class ejercicio1 {
    public static void main(String[] args) {

        int a = 10;
        int b = 5;
        int suma = a + b;
        int resta = a - b;
        int multiplicación = a * b;
        int división = a / b;

        System.out.println("La suma de " + a + " más " + b + " es: " + suma);
        System.out.println("La resta de " + a + " menos " + b + " es: " + resta);
        System.out.println("La multiplicación de " + a + " por " + b + " es: " + multiplicación);
        System.out.println("La división de " + a + " entre " + b + " es: " + división);
    }
}
